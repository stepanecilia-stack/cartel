/**
 * @param {{
 *   inFocusPrep?: boolean,
 *   microPhaseId?: string,
 *   isTransitionDay?: boolean,
 *   athleteStageId?: string | null,
 *   daysUntilFocus?: number | null,
 *   daysUntilAnchor?: number | null,
 *   hasFocusStart?: boolean,
 *   hasAnyStart?: boolean,
 *   seasonGoal?: import('./seasonGoals.js').SeasonGoalId | string | null,
 *   nextSeasonGoal?: import('./seasonGoals.js').SeasonGoalId | string | null,
 *   ladderClosed?: boolean,
 *   focusNewCycle?: boolean,
 *   anchorCertainty?: import('../utils/plannedCompetitions.js').CompetitionDateStatus | 'none',
 *   orientirDaysUntil?: number | null,
 * }} ctx
 */
export function resolveCoachBriefForDay(ctx) {
  const ladderClosed = Boolean(ctx.ladderClosed)
  const mode = resolveSeasonMode({
    seasonGoal: ctx.seasonGoal,
    nextSeasonGoal: ctx.nextSeasonGoal,
    ladderClosed,
  })

  const stageId = ctx.athleteStageId ?? 'between'

  if (
    ctx.inFocusPrep &&
    ctx.microPhaseId &&
    seasonModeAllowsMicroPrep(mode, { ladderClosed, focusNewCycle: ctx.focusNewCycle })
  ) {
    const id = ctx.isTransitionDay ? 'transition' : ctx.microPhaseId
    const brief = getMicroCoachBrief(id) ?? getAthleteStageBrief(`micro-${id}`, mode)
    return {
      kind: 'micro',
      phaseId: id,
      brief,
      seasonMode: mode,
      stageLabel: getMicroStageLabel(id),
    }
  }

  const brief = getAthleteStageBrief(stageId, mode)
  let note = null

  if (ctx.anchorCertainty === 'orientir') {
    const approx =
      ctx.orientirDaysUntil != null && ctx.orientirDaysUntil >= 0
        ? ` Ориентир ~${ctx.orientirDaysUntil} дн — этапы по нарастанию, не жёсткий отсчёт.`
        : ' Ориентир без точной даты — этапы по нарастанию нагрузки.'
    note = `Старт пока ориентир.${approx} После подтверждения даты пересчитаем план и микроцикл.`
  } else {
    const daysUntil = ladderClosed ? ctx.daysUntilAnchor : ctx.daysUntilFocus
    if (daysUntil != null && daysUntil >= 0) {
      note = `До подтверждённого старта: ${daysUntil} дн.`
    }
  }
  if (!note) {
    if (stageId === 'open' || !ctx.hasAnyStart) {
      note = 'Добавьте старт в календарь — после подтверждения даты этапы посчитаются от старта назад.'
    } else if (!ctx.hasFocusStart && ctx.hasAnyStart) {
      note = 'Выберите фокусный старт — после подтверждения даты появится детальный микроплан.'
    } else if (ladderClosed && !ctx.focusNewCycle && (mode === 'advance' || mode === 'peak')) {
      note = 'Для микроплана отметьте город нового цикла и сделайте его фокусом.'
    }
  }

  return {
    kind: 'stage',
    phaseId: stageId,
    brief,
    farFromStart: note,
    seasonMode: mode,
    ladderClosed,
  }
}
